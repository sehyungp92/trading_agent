def _score_phase_candidate_worker(task: tuple[str, dict[str, Any]]) -> ScoredCandidate:
    name, mutations = task
    score_value, rejected, reason = _worker_score(mutations)
    return ScoredCandidate(
        name=name,
        score=0.0 if rejected else float(score_value),
        rejected=bool(rejected),
        reject_reason=str(reason or ""),
        metrics={},
    )


def _metrics_to_dict(metrics: PerformanceMetrics) -> dict[str, float]:
    return {key: getattr(metrics, key) for key in metrics.__dataclass_fields__}


class PortfolioSynergyPhasePlugin:
    name = "portfolio_synergy"
    num_phases = len(PHASES)

    def __init__(
        self,
        data_dir: Path,
        *,
        initial_equity: float = STARTING_EQUITY,
        max_workers: int | None = 2,
        initial_mutations: dict[str, Any] | None = None,
        base_source: str = "phase_source_configs_atrss_round_3_helix_round_5",
    ) -> None:
        self.data_dir = Path(data_dir)
        self.initial_equity = float(initial_equity)
        self.max_workers = max_workers
        self.initial_mutations = dict(initial_mutations or phase_latest_strategy_mutations())
        self.base_source = base_source
        self.initial_mutations_source = base_source
        self._data = None
        self._cached_bundle: ReplayBundle[UnifiedPortfolioData] | None = None
        self._cache_source_fingerprint = ""
        self._metrics_cache: dict[str, dict[str, Any]] = {}
        self._score_cache: dict[str, ScoredCandidate] = {}
        self._provenance: AutoRunProvenance | None = None

    def build_provenance(self) -> AutoRunProvenance:
        if self._provenance is None:
            source_artifacts = {
                f"{sid}:optimized_config": path
                for sid, path in PHASE_SOURCE_CONFIGS.items()
            }
            source_artifacts.update(
                {
                    f"{sid}:diagnostics": path
                    for sid, path in PHASE_SOURCE_DIAGNOSTICS.items()
                }
            )
            self._provenance = build_phase_auto_provenance(
                self.name,
                repo_root=ROOT,
                code_dirs=(Path(__file__).resolve().parent,),
                code_paths=(
                    Path(__file__).resolve(),
                    ROOT / "backtests/swing/engine/unified_portfolio_engine.py",
                    ROOT / "strategies/swing/overlay/engine.py",
                    ROOT / "libs/oms/risk/portfolio_rules.py",
                    ROOT / "libs/oms/risk/swing_portfolio_adapter.py",
                ),
                data_dir=self.data_dir,
                source_artifacts=source_artifacts,
                selection_context={
                    "initial_equity": self.initial_equity,
                    "base_source": self.base_source,
                    "risk_stance": RISK_STANCE,
                    "score_profile": SCORE_PROFILE,
                    "return_basis": OPTIMIZATION_RETURN_BASIS,
                    "replay_architecture": REPLAY_ARCHITECTURE,
                    "phase_scoring_kwargs": PHASE_SCORING_KWARGS,
                    "phase_count": self.num_phases,
                    "round_baseline_policy": "run_spec.baseline_mutations",
                },
            )
        return self._provenance

    @property
    def ultimate_targets(self) -> dict[str, float]:
        return {
            "net_return_pct": float(PHASE_SCORING_KWARGS["alpha_return_target_pct"]),
            "total_trades": float(PHASE_SCORING_KWARGS.get("trade_count_target", PHASE_SCORING_KWARGS["min_trades"])),
            "profit_factor": float(PHASE_SCORING_KWARGS["min_profit_factor"]),
            "max_drawdown_pct": float(PHASE_SCORING_KWARGS["max_drawdown_hard_pct"]) * 100.0,
            "heat_max_R": 4.75,
        }

    def get_phase_spec(self, phase: int, state) -> PhaseSpec:
        del state
        phase_name, candidate_pairs = PHASES[phase - 1]
        return PhaseSpec(
            focus=phase_name,
            candidates=[Experiment(name=name, mutations=dict(mutations)) for name, mutations in candidate_pairs],
            gate_criteria_fn=self._gate_criteria,
            scoring_weights=dict(PHASE_SCORING_KWARGS["score_weights"]),
            hard_rejects={
                "max_drawdown_hard_pct": PHASE_SCORING_KWARGS["max_drawdown_hard_pct"],
                "min_profit_factor": PHASE_SCORING_KWARGS["min_profit_factor"],
                "min_trades": PHASE_SCORING_KWARGS["min_trades"],
                "min_required_strategy_trades": PHASE_SCORING_KWARGS["min_required_strategy_trades"],
                "max_single_strategy_static_pnl_share": PHASE_SCORING_KWARGS[
                    "max_single_strategy_static_pnl_share"
                ],
            },
            analysis_policy=PhaseAnalysisPolicy(
                focus_metrics=[
                    "net_return_pct",
                    "total_trades",
                    "profit_factor",
                    "max_drawdown_pct",
                    "heat_max_R",
                ],
                min_effective_score_delta_pct=0.005,
                diagnostic_gap_fn=_phase_diagnostic_gaps,
            ),
            max_rounds=12,
            prune_threshold=0.02,
            reject_streak_limit=1,
        )

    def create_evaluate_batch(
        self,
        phase: int,
        cumulative_mutations: dict[str, Any],
        *,
        scoring_weights: dict[str, float] | None = None,
        hard_rejects: dict[str, float] | None = None,
    ):
        del cumulative_mutations
        scoring_kwargs = dict(PHASE_SCORING_KWARGS)
        scoring_kwargs.update(hard_rejects or {})
        if scoring_weights:
            scoring_kwargs["score_weights"] = dict(scoring_weights)
        if len(scoring_kwargs.get("score_weights", {})) > 7:
            raise ValueError("Swing portfolio synergy scoring cannot use more than 7 components.")
        bundle = self._ensure_bundle()
        evaluation_key = build_cache_key(
            "swing.portfolio_synergy.evaluation",
            source_fingerprint=bundle.cache_source_fingerprint,
            extra={
                "phase": phase,
                "score_profile": SCORE_PROFILE,
                "score_components": list(scoring_kwargs.get("score_weights", {}).keys()),
                "scoring_weights": scoring_weights or {},
                "hard_rejects": hard_rejects or {},
                "return_basis": OPTIMIZATION_RETURN_BASIS,
                "initial_equity": self.initial_equity,
                "risk_stance": RISK_STANCE,
                "data_dir": str(self.data_dir.resolve()),
            },
        )

        def _local(candidates: list[Experiment], current_mutations: dict[str, Any]) -> list[ScoredCandidate]:
            results: list[ScoredCandidate] = []
            for candidate in candidates:
                merged = dict(current_mutations)
                merged.update(candidate.mutations)
                score, metrics, score_net_profit = _score_config(
                    self._replay_data(),
                    merged,
                    self.initial_equity,
                    OPTIMIZATION_RETURN_BASIS,
                    scoring_kwargs,
                    SCORE_PROFILE,
                )
                metrics_dict = _metrics_to_dict(metrics)
                metrics_dict["score_net_profit"] = float(score_net_profit)
                results.append(
                    ScoredCandidate(
                        name=candidate.name,
                        score=0.0 if score.rejected else float(score.total),
                        rejected=bool(score.rejected),
                        reject_reason=str(score.reject_reason or ""),
                        metrics=metrics_dict,
                    )
                )
            return results

        local = CachedBatchEvaluator(
            _local,
            cache=self._score_cache,
            metrics_cache=self._metrics_cache,
            signature_prefix=f"{self.name}:local:{evaluation_key}",
        )
        if resolve_worker_processes(self.max_workers) <= 1:
            return local

        def _build_args(candidates: list[Experiment], current_mutations: dict[str, Any]):
            args: list[tuple[str, dict[str, Any]]] = []
            for candidate in candidates:
                merged = dict(current_mutations)
                merged.update(candidate.mutations)
                args.append((candidate.name, merged))
            return args

        def _parallel_factory():
            pool = create_process_pool(
                self.max_workers,
                initializer=_init_worker,
                initargs=(
                    str(self.data_dir),
                    self.initial_equity,
                    OPTIMIZATION_RETURN_BASIS,
                    scoring_kwargs,
                    SCORE_PROFILE,
                ),
                logger=logging.getLogger(__name__),
                description="portfolio synergy phase auto",
            )
            return SharedPoolBatchEvaluator(
                pool,
                worker_fn=_score_phase_candidate_worker,
                build_args=_build_args,
                on_terminate=lambda: shutdown_process_pool(pool, force=True),
                on_close=lambda: shutdown_process_pool(pool, force=False),
                description="portfolio synergy candidate batch",
                logger=logging.getLogger(__name__),
                per_candidate_timeout_seconds=420.0,
                minimum_timeout_seconds=420.0,
            )

        parallel = ResilientBatchEvaluator(
            preferred_factory=_parallel_factory,
            fallback_factory=lambda: local,
            description="portfolio synergy parallel evaluator",
            logger=logging.getLogger(__name__),
        )
        return CachedBatchEvaluator(
            parallel,
            cache=self._score_cache,
            metrics_cache=self._metrics_cache,
            signature_prefix=f"{self.name}:parallel:{evaluation_key}",
        )

    def compute_final_metrics(self, mutations: dict[str, Any]) -> dict[str, Any]:
        cache_key = mutation_signature(mutations)
        cached = self._metrics_cache.get(cache_key)
        if cached is not None and "strategy_summary" in cached:
            return self._with_replay_metadata(dict(cached))
        _, _, metrics = _evaluate(
            self._replay_data(),
            mutations,
            equity=self.initial_equity,
            data_dir=self.data_dir,
        )
        metrics = self._with_replay_metadata(metrics)
        self._metrics_cache[cache_key] = dict(metrics)
        return metrics

