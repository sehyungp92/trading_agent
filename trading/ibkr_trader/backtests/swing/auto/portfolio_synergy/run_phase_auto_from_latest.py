    def _ensure_bundle(self) -> ReplayBundle[UnifiedPortfolioData]:
        source_fingerprint = stable_signature(
            {
                "data_tree": fingerprint_tree(self.data_dir, patterns=("*.parquet",), recursive=False),
                "strategy_configs": fingerprint_paths(PHASE_SOURCE_CONFIGS.values(), root=ROOT),
                "initial_mutations": stable_signature(self.initial_mutations),
                "base_source": self.base_source,
                "initial_equity": self.initial_equity,
                "score_profile": SCORE_PROFILE,
                "return_basis": OPTIMIZATION_RETURN_BASIS,
                "risk_stance": RISK_STANCE,
            }
        )
        cache_key = build_cache_key(
            "swing.portfolio_synergy.replay_bundle",
            source_fingerprint=source_fingerprint,
            extra={
                "data_dir": str(self.data_dir.resolve()),
                "initial_equity": self.initial_equity,
                "round": ROUND_NAME,
            },
        )
        if self._cached_bundle is not None and self._cached_bundle.cache_key == cache_key:
            return self._cached_bundle
        if self._cache_source_fingerprint and self._cache_source_fingerprint != source_fingerprint:
            self._metrics_cache.clear()
            self._score_cache.clear()
        seed_config = UnifiedBacktestConfig(initial_equity=self.initial_equity, data_dir=self.data_dir)
        self._data = load_unified_data(seed_config)
